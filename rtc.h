        if (!valid) {
            valid = true;
            desc.vs.source = (const char*)vs_source_wgsl;
            desc.vs.entry = "main";
            desc.fs.source = (const char*)fs_source_wgsl;
            desc.fs.entry = "main";
            desc.fs.uniform_blocks[0].size = 16;
            desc.fs.uniform_blocks[0].layout = SG_UNIFORMLAYOUT_STD140;
            desc.label = "rtc_shader";
        }
